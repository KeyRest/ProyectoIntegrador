package Modelo;

import java.io.Serializable;
import java.util.Collection;
import javax.persistence.Basic;
import javax.persistence.Column;
import javax.persistence.Entity;
import javax.persistence.Id;
import javax.persistence.ManyToMany;
import javax.persistence.NamedQueries;
import javax.persistence.NamedQuery;
import javax.persistence.Table;
import javax.xml.bind.annotation.XmlRootElement;
import javax.xml.bind.annotation.XmlTransient;




@Entity
@Table(name = "ocasion")
@XmlRootElement
@NamedQueries({
    @NamedQuery(name = "Ocasion.findAll", query = "SELECT o FROM Ocasion o"),
    @NamedQuery(name = "Ocasion.findById", query = "SELECT o FROM Ocasion o WHERE o.id = :id"),
    @NamedQuery(name = "Ocasion.findBySituacion", query = "SELECT o FROM Ocasion o WHERE o.situacion = :situacion")})
public class Ocasion implements Serializable {

    private static final long serialVersionUID = 1L;
    @Id
    @Basic(optional = false)
    @Column(name = "id")
    private Integer id;
    @Column(name = "situacion")
    private String situacion;
    @ManyToMany(mappedBy = "ocasionCollection")
    private Collection<Receta> recetaCollection;

    public Ocasion() {
    }

    public Ocasion(Integer id) {
        this.id = id;
    }

    public Integer getId() {
        return id;
    }

    public void setId(Integer id) {
        this.id = id;
    }

    public String getSituacion() {
        return situacion;
    }

    public void setSituacion(String situacion) {
        this.situacion = situacion;
    }

    @XmlTransient
    public Collection<Receta> getRecetaCollection() {
        return recetaCollection;
    }

    public void setRecetaCollection(Collection<Receta> recetaCollection) {
        this.recetaCollection = recetaCollection;
    }

    @Override
    public int hashCode() {
        int hash = 0;
        hash += (id != null ? id.hashCode() : 0);
        return hash;
    }

    @Override
    public boolean equals(Object object) {
        // TODO: Warning - this method won't work in the case the id fields are not set
        if (!(object instanceof Ocasion)) {
            return false;
        }
        Ocasion other = (Ocasion) object;
        if ((this.id == null && other.id != null) || (this.id != null && !this.id.equals(other.id))) {
            return false;
        }
        return true;
    }

    @Override
    public String toString() {
        return "Modelo.Ocasion[ id=" + id + " ]";
    }

}
